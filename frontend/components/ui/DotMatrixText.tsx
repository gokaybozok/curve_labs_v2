interface DotMatrixTextProps {
  text?: string;
  dotSize?: number;
  gap?: number;
  color?: string;
}

export const DotMatrixText: React.FC<DotMatrixTextProps> = ({
  text = 'Digital Souls',
  dotSize = 3,
  gap = 1,
  color = 'rgba(161, 161, 170, 0.9)',
}) => {
  const containerRef = useRef<HTMLDivElement>(null);
  const [scale, setScale] = useState(1);

  const generateMatrix = () => {
    const chars = text.split('');
    const letterSpacing = 3;
    let totalWidth = 0;
    const maxHeight = 36;
    const charMatrices: { pattern: number[][]; startX: number }[] = [];

    chars.forEach((char) => {
      const pattern = GOTHIC_PATTERNS[char] || GOTHIC_PATTERNS[' '];
      charMatrices.push({ pattern, startX: totalWidth });
      totalWidth += pattern[0]?.length || 14;
      totalWidth += letterSpacing;
    });

    totalWidth -= letterSpacing;
    return { charMatrices, totalWidth, maxHeight };
  };

  const { charMatrices, totalWidth, maxHeight } = generateMatrix();

  useEffect(() => {
    const updateScale = () => {
      if (containerRef.current) {
        const containerWidth = containerRef.current.offsetWidth;
        const matrixWidth = totalWidth * (dotSize + gap);
        const newScale = Math.min(1, (containerWidth - 40) / matrixWidth);
        setScale(newScale);
      }
    };

    updateScale();
    window.addEventListener('resize', updateScale);
    return () => window.removeEventListener('resize', updateScale);
  }, [totalWidth, dotSize, gap]);

  const renderDots = () => {
    const dots: React.ReactNode[] = [];
    let dotIndex = 0;

    charMatrices.forEach(({ pattern, startX }) => {
      pattern.forEach((row, rowIndex) => {
        row.forEach((cell, colIndex) => {
          if (cell === 1) {
            const x = (startX + colIndex) * (dotSize + gap);
            const y = rowIndex * (dotSize + gap);
            dots.push(
              <circle
                key={dotIndex++}
                cx={x + dotSize / 2}
                cy={y + dotSize / 2}
                r={dotSize / 2}
                fill={color}
              />
            );
          }
        });
      });
    });

    return dots;
  };

  const svgWidth = totalWidth * (dotSize + gap);
  const svgHeight = maxHeight * (dotSize + gap);

  return (
    <div
      ref={containerRef}
      className="w-full flex items-center justify-center overflow-hidden"
    >
      <svg
        width={svgWidth * scale}
        height={svgHeight * scale}
        viewBox={`0 0 ${svgWidth} ${svgHeight}`}
        className="max-w-full"
        style={{
          filter: 'drop-shadow(0 0 6px rgba(161, 161, 170, 0.15))',
        }}
      >
        {renderDots()}
      </svg>
    </div>
  );
};
